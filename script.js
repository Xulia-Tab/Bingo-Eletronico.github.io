function configurarEventos() {
  el.sortearBtn.addEventListener("click", sortearComAnimacao);
  el.desfazerBtn.addEventListener("click", desfazer);
  el.reiniciarBtn.addEventListener("click", reiniciar);
  el.salvarBtn.addEventListener("click", salvarLista);
  el.telaCheiaBtn.addEventListener("click", alternarTelaCheia);

  el.modoSelect.addEventListener("change", () => {
    estado.modoBingo = el.modoSelect.value;
    estado.vencedoresAnunciados.clear();
    atualizarPainelGanhadores(false);
  });

  el.fecharModalBtn.addEventListener("click", fecharModal);
  el.okModalBtn.addEventListener("click", fecharModal);
  el.modalBingo.addEventListener("click", (event) => {
    if (event.target === el.modalBingo) fecharModal();
  });

  document.addEventListener("keydown", (event) => {
    const alvoEditavel = ["INPUT", "TEXTAREA", "SELECT", "BUTTON"].includes(event.target.tagName);

    if (event.code === "Space" && !alvoEditavel) {
      event.preventDefault();
      sortearComAnimacao();
    }

    if (event.key === "F11") {
      event.preventDefault();
      alternarTelaCheia();
    }

    if (event.key === "Escape") {
      fecharModal();
      if (document.fullscreenElement) document.exitFullscreen();
    }
  });
}

function criarIntervalo(inicio, fim) {
  return Array.from({ length: fim - inicio + 1 }, (_, i) => inicio + i);
}

function criarGradeNumeros() {
  el.gradeNumeros.innerHTML = "";
  gradeLabels.clear();

  for (let numero = NUMERO_INICIAL; numero <= NUMERO_FINAL; numero += 1) {
    const item = document.createElement("div");
    item.className = "numero-grade";
    item.textContent = numero;
    item.setAttribute("aria-label", `Número ${numero}`);
    el.gradeNumeros.appendChild(item);
    gradeLabels.set(numero, item);
  }
}

