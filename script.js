function criarBolinhasGlobo() {
  const cores = [
    CORES.rosa,
    CORES.laranja,
    CORES.verde,
    CORES.azul,
    CORES.roxo,
    CORES.vermelho,
    "#14b8a6",
    "#facc15",
  ];

  estado.bolas = [];
  for (let i = 0; i < 22; i += 1) {
    estado.bolas.push({
      x: numeroAleatorio(-90, 90),
      y: numeroAleatorio(-55, 55),
      vx: numeroAleatorio(-1.8, 1.8),
      vy: numeroAleatorio(-1.6, 1.6),
      r: Math.floor(numeroAleatorio(9, 14)),
      cor: escolherAleatorio(cores),
      numero: Math.floor(numeroAleatorio(NUMERO_INICIAL, NUMERO_FINAL + 1)),
    });
  }
}

function numeroAleatorio(min, max) {
  return Math.random() * (max - min) + min;
}

function desenharGlobo() {
  const ctx = contextoGlobo;
  ctx.clearRect(0, 0, el.canvasGlobo.width, el.canvasGlobo.height);

  const cx = 175;
  const cy = 138;
  const raioX = 135;
  const raioY = 92;
  const velocidade = estado.animandoSorteio ? 2.4 : 1.0;

  desenharElipse(ctx, cx + 15, cy + 137, 130, 12, "#d1d5db", "transparent", 0);
  desenharRetangulo(ctx, cx - 78, cy + 108, 156, 20, "#374151");
  desenharRetangulo(ctx, cx - 48, cy + 88, 96, 22, "#6b7280");
  desenharRetangulo(ctx, cx - 95, cy + 126, 190, 12, CORES.preto);

  ctx.beginPath();
  ctx.moveTo(cx + raioX - 8, cy + 18);
  ctx.lineTo(cx + raioX + 72, cy + 32);
  ctx.lineTo(cx + raioX + 72, cy + 56);
  ctx.lineTo(cx + raioX - 5, cy + 42);
  ctx.closePath();
  ctx.fillStyle = "#cbd5e1";
  ctx.fill();
  ctx.strokeStyle = "#64748b";
  ctx.lineWidth = 2;
  ctx.stroke();

  desenharElipse(ctx, cx, cy, raioX + 5, raioY + 5, "#dbeafe", "#1d4ed8", 5);
  desenharElipse(ctx, cx, cy, raioX, raioY, "#eff6ff", "#60a5fa", 2);

  estado.anguloGlobo += 0.08 * velocidade;

  for (let k = 0; k < 6; k += 1) {
    const angulo = estado.anguloGlobo + (k * Math.PI) / 6;
    const x1 = cx + Math.cos(angulo) * raioX;
    const y1 = cy + Math.sin(angulo) * raioY;
    const x2 = cx - Math.cos(angulo) * raioX;
    const y2 = cy - Math.sin(angulo) * raioY;

    ctx.beginPath();
    ctx.moveTo(x1, y1);
    ctx.lineTo(x2, y2);
    ctx.strokeStyle = "#93c5fd";
    ctx.lineWidth = 2;
    ctx.stroke();
  }

  for (const bola of estado.bolas) {
    bola.x += bola.vx * velocidade;
    bola.y += bola.vy * velocidade;

    const limite = (bola.x / raioX) ** 2 + (bola.y / raioY) ** 2;
    if (limite > 0.72) {
      bola.vx *= -1;
      bola.vy *= -1;
      bola.x += bola.vx * 3;
      bola.y += bola.vy * 3;
    }

    const x = cx + bola.x;
    const y = cy + bola.y;

    desenharElipse(ctx, x, y, bola.r, bola.r, bola.cor, "#ffffff", 2);
    ctx.fillStyle = "#ffffff";
    ctx.font = "bold 7px Arial";
    ctx.textAlign = "center";
    ctx.textBaseline = "middle";
    ctx.fillText(String(bola.numero), x, y);
  }

  desenharElipse(ctx, cx, cy, raioX, raioY, "transparent", CORES.azul, 4);
  window.requestAnimationFrame(desenharGlobo);
}

function desenharElipse(ctx, x, y, raioX, raioY, preenchimento, contorno, larguraContorno) {
  ctx.beginPath();
  ctx.ellipse(x, y, raioX, raioY, 0, 0, Math.PI * 2);
  if (preenchimento !== "transparent") {
    ctx.fillStyle = preenchimento;
    ctx.fill();
  }
  if (larguraContorno > 0 && contorno !== "transparent") {
    ctx.strokeStyle = contorno;
    ctx.lineWidth = larguraContorno;
    ctx.stroke();
  }
}

function desenharRetangulo(ctx, x, y, largura, altura, preenchimento) {
  ctx.fillStyle = preenchimento;
  ctx.fillRect(x, y, largura, altura);
}
